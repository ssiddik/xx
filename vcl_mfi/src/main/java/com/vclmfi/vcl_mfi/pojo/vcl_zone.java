package com.vclmfi.vcl_mfi.pojo;

public class vcl_zone {
	
	private int zone_id;
	private String zone_short_code;
	private String zone_long_name;
	
	public vcl_zone() {
		super();
	}

	public int getZone_id() {
		return zone_id;
	}

	public void setZone_id(int zone_id) {
		this.zone_id = zone_id;
	}

	public String getZone_short_code() {
		return zone_short_code;
	}

	public void setZone_short_code(String zone_short_code) {
		this.zone_short_code = zone_short_code;
	}

	public String getZone_long_name() {
		return zone_long_name;
	}

	public void setZone_long_name(String zone_long_name) {
		this.zone_long_name = zone_long_name;
	}
	

}

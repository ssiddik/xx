package com.vclmfi.vcl_mfi.pojo;

public class vcl_employee_role {
	
	private int emp_role_id;
	private String emp_role_code;
	private String emp_role_name;
	private String emp_role_desc;
	
	public vcl_employee_role() {
		super();
	}

	public int getEmp_role_id() {
		return emp_role_id;
	}

	public void setEmp_role_id(int emp_role_id) {
		this.emp_role_id = emp_role_id;
	}

	public String getEmp_role_code() {
		return emp_role_code;
	}

	public void setEmp_role_code(String emp_role_code) {
		this.emp_role_code = emp_role_code;
	}

	public String getEmp_role_name() {
		return emp_role_name;
	}

	public void setEmp_role_name(String emp_role_name) {
		this.emp_role_name = emp_role_name;
	}

	public String getEmp_role_desc() {
		return emp_role_desc;
	}

	public void setEmp_role_desc(String emp_role_desc) {
		this.emp_role_desc = emp_role_desc;
	}

}
